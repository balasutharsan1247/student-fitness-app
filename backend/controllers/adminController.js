// @desc    Assign students to a mentor
exports.assignStudents = async (req, res) => {
  try {
    const { mentorId } = req.params;
    const { studentIds } = req.body;

    if (!Array.isArray(studentIds) || studentIds.length === 0) {
      return res.status(400).json({
        success: false,
        message: 'studentIds array is required',
      });
    }

    const uniqueStudentIds = [...new Set(studentIds)];

    const mentor = await User.findById(mentorId);
    if (!mentor || mentor.role !== 'mentor') {
      return res.status(404).json({
        success: false,
        message: 'Mentor not found',
      });
    }

    const students = await User.find({ _id: { $in: uniqueStudentIds }, role: 'student' });
    if (students.length !== uniqueStudentIds.length) {
      return res.status(404).json({
        success: false,
        message: 'One or more student IDs are invalid or not students',
      });
    }

    const updatedStudentIds = new Set(
      mentor.assignedStudents.map((id) => id.toString())
    );

    const previousMentorIds = [
      ...new Set(
        students
          .map((student) => student.assignedMentor?.toString())
          .filter((id) => id && id !== mentor._id.toString())
      ),
    ];

    if (previousMentorIds.length > 0) {
      await User.updateMany(
        { _id: { $in: previousMentorIds } },
        { $pull: { assignedStudents: { $in: students.map((student) => student._id) } } }
      );
    }

    await Promise.all(
      students.map(async (student) => {
        student.assignedMentor = mentor._id;
        await student.save();
        updatedStudentIds.add(student._id.toString());
      })
    );

    mentor.assignedStudents = Array.from(updatedStudentIds);
    await mentor.save();

    res.status(200).json({
      success: true,
      message: 'Students assigned successfully',
      data: {
        mentorId: mentor._id,
        assignedStudents: mentor.assignedStudents,
      },
    });
  } catch (error) {
    console.error('Assign Students Error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error while assigning students',
      error: error.message,
    });
  }
};

// @desc    Unassign a student from a mentor
exports.unassignStudent = async (req, res) => {
  try {
    const { mentorId, studentId } = req.params;

    const mentor = await User.findById(mentorId);
    if (!mentor || mentor.role !== 'mentor') {
      return res.status(404).json({
        success: false,
        message: 'Mentor not found',
      });
    }

    const student = await User.findById(studentId);
    if (!student || student.role !== 'student') {
      return res.status(404).json({
        success: false,
        message: 'Student not found',
      });
    }

    mentor.assignedStudents = (mentor.assignedStudents || []).filter(
      (id) => id.toString() !== student._id.toString()
    );

    if (student.assignedMentor?.toString() === mentor._id.toString()) {
      student.assignedMentor = null;
      await student.save();
    }

    await mentor.save();

    res.status(200).json({
      success: true,
      message: 'Student unassigned successfully',
      data: {
        mentorId: mentor._id,
        studentId: student._id,
      },
    });
  } catch (error) {
    console.error('Unassign Student Error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error while unassigning student',
      error: error.message,
    });
  }
};

// @desc    Get aggregate university-wide wellness statistics
// @route   GET /api/admin/aggregate-stats
// @access  Private (Admin/Mentor only)
exports.getAggregateStats = async (req, res) => {
  try {
    // Only allow admin and mentor to access this route
    if (req.user.role !== 'admin' && req.user.role !== 'mentor') {
      return res.status(403).json({
        success: false,
        message: 'Not authorized to access aggregated stats',
      });
    }

    // 1. Get Population Stats (System Engagement)
    const totalUsers = await User.countDocuments({ role: 'student' });
    const DAU = await User.countDocuments({ 
        role: 'student', 
        lastLogDate: { $gte: new Date(new Date().setHours(0,0,0,0)) } 
    });

    const activeUsers = await User.find({ role: 'student', currentStreak: { $gt: 0 } });
    const avgStreak = activeUsers.length > 0 
        ? Math.round(activeUsers.reduce((sum, u) => sum + (u.currentStreak || 0), 0) / activeUsers.length)
        : 0;

    // 1b. Mentor Assignment Summary
    const totalMentors = await User.countDocuments({ role: 'mentor' });
    const unassignedStudents = await User.countDocuments({ role: 'student', assignedMentor: null });
    const mentors = await User.find({ role: 'mentor' })
      .select('firstName lastName mentorId assignedStudents')
      .lean();
    const mentorLoad = mentors.map((m) => ({
      name: `${m.firstName} ${m.lastName}`,
      mentorId: m.mentorId || null,
      studentCount: (m.assignedStudents || []).length,
    }));

    // 2. Department-level Activity & Stress Averages over the last 30 days
    const thirtyDaysAgo = new Date();
    thirtyDaysAgo.setDate(thirtyDaysAgo.getDate() - 30);

    const departmentStats = await FitnessLog.aggregate([
      {
        $match: { date: { $gte: thirtyDaysAgo } }
      },
      {
        // Join with the User collection to get department and role
        $lookup: {
          from: 'users',
          localField: 'user',
          foreignField: '_id',
          as: 'userDetails'
        }
      },
      {
        $unwind: '$userDetails'
      },
      {
        $match: { 'userDetails.role': 'student' } // Only count students
      },
      {
        // Group by department
        $group: {
          _id: '$userDetails.department',
          avgStressLevel: { $avg: '$stressLevel' },
          avgSleepHours: { $avg: '$sleep.hours' },
          avgActiveMinutes: { $avg: '$activeMinutes' },
          logCount: { $sum: 1 }
        }
      },
      {
        $project: {
            department: { $ifNull: ['$_id', 'Unknown'] },
            avgStressLevel: { $round: ['$avgStressLevel', 1] },
            avgSleepHours: { $round: ['$avgSleepHours', 1] },
            avgActiveMinutes: { $round: ['$avgActiveMinutes', 0] },
            logCount: 1,
            _id: 0
        }
      },
      { $sort: { logCount: -1 } }
    ]);

    // 3. Overall Campus Stress Timeseries (last 14 days)
    const fourteenDaysAgo = new Date();
    fourteenDaysAgo.setDate(fourteenDaysAgo.getDate() - 14);

    const stressTimeseries = await FitnessLog.aggregate([
      {
         $match: { date: { $gte: fourteenDaysAgo }, stressLevel: { $exists: true } }
      },
      {
         $group: {
            _id: { $dateToString: { format: "%Y-%m-%d", date: "$date" } },
            avgStress: { $avg: '$stressLevel' },
            avgSleep: { $avg: '$sleep.hours' }
         }
      },
      {
         $project: {
            date: '$_id',
            avgStress: { $round: ['$avgStress', 1] },
            avgSleep: { $round: ['$avgSleep', 1] },
            _id: 0
         }
      },
      { $sort: { date: 1 } }
    ]);

    res.status(200).json({
      success: true,
      data: {
        engagement: {
            totalStudents: totalUsers,
            dailyActiveUsers: DAU,
            engagementRate: totalUsers > 0 ? Math.round((DAU / totalUsers) * 100) : 0,
            averageCurrentStreak: avgStreak
        },
        mentorSummary: {
            totalMentors,
            unassignedStudents,
            mentorLoad,
        },
        departmentStats,
        stressTimeseries
      },
    });
  } catch (error) {
    console.error('Aggregate Stats Error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error while aggregating stats',
      error: error.message,
    });
  }
};
