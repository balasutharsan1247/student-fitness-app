const User = require('../models/User');
const FitnessLog = require('../models/FitnessLog');

const ROLE_ID_PREFIX = {
  student: 'STU',
  mentor: 'MEN',
  admin: 'ADM',
};

const getRoleIdField = (role) => {
  if (role === 'mentor') return 'mentorId';
  if (role === 'admin') return 'adminId';
  return 'studentId';
};

const getAccountId = (user) => {
  return user.studentId || user.mentorId || user.adminId || null;
};

const generateRoleId = async (role) => {
  const prefix = ROLE_ID_PREFIX[role] || ROLE_ID_PREFIX.student;
  const currentYear = new Date().getFullYear();
  let isUnique = false;
  let newId = '';

  while (!isUnique) {
    const randomDigits = Math.floor(1000 + Math.random() * 9000);
    newId = `${prefix}${currentYear}${randomDigits}`;

    const existing = await User.findOne({
      $or: [
        { studentId: newId },
        { mentorId: newId },
        { adminId: newId },
      ],
    });

    if (!existing) isUnique = true;
  }

  return newId;
};

const sanitizeAcademicFields = (data, role) => {
  if (role !== 'student') {
    delete data.university;
    delete data.department;
    delete data.graduateType;
    delete data.year;
    delete data.dateOfBirth;
    delete data.age;
    delete data.gender;
  }
  return data;
};

const formatUser = (user) => ({
  _id: user._id,
  firstName: user.firstName,
  lastName: user.lastName,
  email: user.email,
  role: user.role,
  university: user.university || '',
  department: user.department || '',
  graduateType: user.graduateType || '',
  year: user.year || '',
  dateOfBirth: user.dateOfBirth || null,
  age: user.age || null,
  gender: user.gender || '',
  isActive: user.isActive,
  points: user.points || 0,
  level: user.role === 'student' ? Math.floor((user.points || 0) / 500) + 1 : null,
  accountId: getAccountId(user),
  createdAt: user.createdAt,
  updatedAt: user.updatedAt,
});

// @desc    Get all users and their role details
exports.getUsers = async (req, res) => {
  try {
    const users = await User.find()
      .select('firstName lastName email role university department graduateType year dateOfBirth age gender isActive points currentStreak studentId mentorId adminId createdAt updatedAt')
      .sort({ role: 1, lastName: 1, firstName: 1 });

    res.status(200).json({
      success: true,
      data: users.map(formatUser),
    });
  } catch (error) {
    console.error('Get Users Error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error while fetching users',
      error: error.message,
    });
  }
};

// @desc    Create a new user from admin panel
exports.createUser = async (req, res) => {
  try {
    const {
      firstName,
      lastName,
      email,
      password,
      role,
      university,
      department,
      graduateType,
      year,
      dateOfBirth,
      age,
      gender,
    } = req.body;

    if (!firstName || !lastName || !email || !password) {
      return res.status(400).json({
        success: false,
        message: 'First name, last name, email, and password are required',
      });
    }

    const existingUser = await User.findOne({ email });
    if (existingUser) {
      return res.status(400).json({
        success: false,
        message: 'A user with that email already exists',
      });
    }

    const targetRole = role || 'student';
    const autoGeneratedId = await generateRoleId(targetRole);
    const idField = getRoleIdField(targetRole);

    const userData = sanitizeAcademicFields({
      firstName,
      lastName,
      email,
      password,
      role: targetRole,
      university,
      department,
      graduateType,
      year,
      dateOfBirth,
      age,
      gender,
      [idField]: autoGeneratedId,
    }, targetRole);

    const user = await User.create(userData);

    res.status(201).json({
      success: true,
      data: formatUser(user),
    });
  } catch (error) {
    console.error('Create User Error:', error);
    if (error.name === 'ValidationError') {
      const messages = Object.values(error.errors).map((err) => err.message);
      return res.status(400).json({
        success: false,
        message: messages.join(', '),
      });
    }
    res.status(500).json({
      success: false,
      message: 'Server error while creating user',
      error: error.message,
    });
  }
};

// @desc    Update user details from admin panel
exports.updateUser = async (req, res) => {
  try {
    const { userId } = req.params;
    const {
      firstName,
      lastName,
      email,
      role,
      university,
      department,
      graduateType,
      year,
      dateOfBirth,
      age,
      gender,
      isActive,
    } = req.body;

    const user = await User.findById(userId);
    if (!user) {
      return res.status(404).json({
        success: false,
        message: 'User not found',
      });
    }

    if (email && email !== user.email) {
      const emailTaken = await User.findOne({ email });
      if (emailTaken) {
        return res.status(400).json({
          success: false,
          message: 'Another account is already using that email',
        });
      }
    }

    const newRole = role || user.role;
    const roleChanged = newRole !== user.role;

    user.firstName = firstName ?? user.firstName;
    user.lastName = lastName ?? user.lastName;
    user.email = email ?? user.email;
    user.role = newRole;
    user.isActive = typeof isActive === 'boolean' ? isActive : user.isActive;

    if (roleChanged) {
      user.studentId = undefined;
      user.mentorId = undefined;
      user.adminId = undefined;
      const newIdField = getRoleIdField(newRole);
      user[newIdField] = await generateRoleId(newRole);
    }

    const updatedData = sanitizeAcademicFields({
      university,
      department,
      graduateType,
      year,
      dateOfBirth,
      age,
      gender,
    }, newRole);

    Object.entries(updatedData).forEach(([key, value]) => {
      if (typeof value !== 'undefined') {
        user[key] = value;
      }
    });

    await user.save();

    res.status(200).json({
      success: true,
      data: formatUser(user),
    });
  } catch (error) {
    console.error('Update User Error:', error);
    if (error.name === 'ValidationError') {
      const messages = Object.values(error.errors).map((err) => err.message);
      return res.status(400).json({
        success: false,
        message: messages.join(', '),
      });
    }
    res.status(500).json({
      success: false,
      message: 'Server error while updating user',
      error: error.message,
    });
  }
};

// @desc    Delete a user from admin panel
exports.deleteUser = async (req, res) => {
  try {
    const { userId } = req.params;

    if (req.user.id === userId) {
      return res.status(400).json({
        success: false,
        message: 'Admins cannot delete their own account',
      });
    }

    const user = await User.findById(userId);
    if (!user) {
      return res.status(404).json({
        success: false,
        message: 'User not found',
      });
    }

    if (user.role === 'mentor' && user.assignedStudents?.length) {
      await User.updateMany(
        { _id: { $in: user.assignedStudents } },
        { $set: { assignedMentor: null } }
      );
    }

    if (user.role === 'student' && user.assignedMentor) {
      await User.findByIdAndUpdate(user.assignedMentor, {
        $pull: { assignedStudents: user._id },
      });
    }

    await User.findByIdAndDelete(userId);

    res.status(200).json({
      success: true,
      message: 'User deleted successfully',
    });
  } catch (error) {
    console.error('Delete User Error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error while deleting user',
      error: error.message,
    });
  }
};

